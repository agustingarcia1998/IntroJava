            //Upper Camel Case, porque la inicial es mayuscula
public class NamingJava {
    public static void main(String[] args) {
        int celphone = 351387744;
        int celPhone = 555511144;
        System.out.println(celphone);
        System.out.println(celPhone);

        //Se pueden declarar así también ( $, _)
        String $countryName = "Spain";
        String _backgroundColor = "Green";

        String currency = "MXN";
        String background_color = "BLUE";

        //Declaracion de variables
        int POSITION = -5;
        int MAX_WIDTH = 9999;
        int MIN_WIDTH = 50;

        //Lower Camel Case, inicia con minuscula
        String fullName = "Agustin Nicolas Garcia";
        int sizeInCentimeters = 26;
    }
}
